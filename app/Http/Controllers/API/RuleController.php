    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(RuleStoreRequest $request)
    {
        $auth = Auth::user();
        $rule = new Rule;

        $rule->primary_expenses = $request->primary_expenses;
        $rule->secundary_expenses = $request->secundary_expenses;
        $rule->saving = $request->saving;
        $rule->user_id = $auth->id;

        if ($rule->save()) {
            return response(array('success' => true, 'insert_id' => $rule->id), 200);
        }
    }

